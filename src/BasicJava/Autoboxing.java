package BasicJava;

public class Autoboxing {
    public static void main(String[] args) {
        Integer a=100;
        Integer b=100;
        System.out.println(a==b);
        System.out.println(a.equals(b));
        System.out.println("----------------------------------------------");
        Integer c=1000;
        Integer d=1000;
        System.out.println(c==d);
        System.out.println(c.equals(d));
    }
}
